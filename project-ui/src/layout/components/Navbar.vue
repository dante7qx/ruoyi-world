<template>
  <div class="navbar">
    <hamburger id="hamburger-container" :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb id="breadcrumb-container" class="breadcrumb-container" v-if="!topNav"/>
    <top-nav id="topmenu-container" class="topmenu-container" v-if="topNav"/>

    <div class="right-menu">
      <template v-if="device!=='mobile'">
        <search id="header-search" class="right-menu-item" />
        <screenfull id="screenfull" class="right-menu-item hover-effect" />
        <el-tooltip content="布局大小" effect="dark" placement="bottom">
          <size-select id="size-select" class="right-menu-item hover-effect" />
        </el-tooltip>
      </template>

      <el-dropdown class="avatar-container right-menu-item hover-effect" trigger="click">
        <div class="avatar-wrapper">
          <img :src="avatar" alt="" class="user-avatar">
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown">
          <router-link to="/user/profile">
            <el-dropdown-item>个人中心</el-dropdown-item>
          </router-link>
          <el-dropdown-item @click.native="setting = true">
            <span>布局设置</span>
          </el-dropdown-item>
          <el-dropdown-item divided @click.native="logout">
            <span>退出登录</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import TopNav from '@/components/TopNav'
import Hamburger from '@/components/Hamburger'
import Screenfull from '@/components/Screenfull'
import SizeSelect from '@/components/SizeSelect'
import Search from '@/components/HeaderSearch'
import store from '@/store'

export default {
  components: {
    Breadcrumb,
    TopNav,
    Hamburger,
    Screenfull,
    SizeSelect,
    Search
  },
  data() {
    return {
      ws: null
    }
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar',
      'device'
    ]),
    setting: {
      get() {
        return this.$store.state.settings.showSettings
      },
      set(val) {
        this.$store.dispatch('settings/changeSetting', {
          key: 'showSettings',
          value: val
        })
      }
    },
    topNav: {
      get() {
        return this.$store.state.settings.topNav
      }
    }
  },
  mounted() {
    // 建立WebSocket连接，进行消息提醒（若项目有此需求，放开该行代码）
    // this.connWS()
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      this.$confirm('确定注销并退出系统吗？', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).then(() => {
        if (this.ws) {
          this.ws.close();
          this.ws = null;
        }
        this.$store.dispatch('LogOut').then(() => {
          location.href = this.$router.options.base == "/" ? "/index" : this.$router.options.base + '/index'
        })
      }).catch(() => {});
    },
    connWS() {
      const user = this.$store.state.user
      if(user == null) {
        return;
      }
      const { host } = location
      const wsuri = `ws://${host}${process.env.VUE_APP_SOCKET_BASE_API}/websocket/message/${user.name}`;
      this.ws = new WebSocket(wsuri);
      const self = this;
      this.ws.onopen = function (event) {
        console.log('已建立WebSocket连接！')
      };
      this.ws.onmessage = function (event) {
        self.notifyUser(event.data)
      };
      this.ws.onclose = function (event) {
        console.log('已关闭WebSocket连接！')
      };
    },
    notifyUser(msg) {
      const data = JSON.parse(msg)
      const that = this
      this.$notify({
        title: data.title,
        type: 'success',
        iconClass: 'el-icon-info',
        message: data.content,
        onClick() {
          that.handleNotifyClick(data)
        }
      });
    },
    handleNotifyClick(data) {
      // 按项目需求实现相关业务逻辑
      console.log(data)
    }
  }
}
</script>

